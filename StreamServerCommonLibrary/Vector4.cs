namespace CommonLibrary
{
    public readonly struct Vector4
    {
        public readonly float x;
        public readonly float y;
        public readonly float z;
        public readonly float w;

        public Vector4(float x, float y, float z, float w)
        {
            this.x = x;
            this.y = y;
            this.z = z;
            this.w = w;
        }
    }
}
